function handleStudentAttendanceKey(e) {
  if (e.key == " " && e.code == "Space") {
    return "absent";
  } else if (e.code == "Enter") {
    return "present";
  } else if (e.key == "ArrowLeft") {
    return "prev";
  } else if (e.key == "ArrowRight") {
    return "next";
  } else if (e.key == "Backspace") {
    return "back";
  } else {
    return "pass";
  }
}
